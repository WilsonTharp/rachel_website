<p>I present the history of the virgule or slash (/) as a measure of how students read and interpreted texts first by methods taught in grammar handbooks but then according to scribal-driven innovations to the page. The mark primarily operates in four ways: as an outsider to <em>distinctiones</em>, the dominant mode of punctuating from the first century CE to the ninth; as the term <em>virgula</em>, a “stem” or “twig” describing the shapes of accents or critical signs in grammatical treatises; as a runover or punctuation mark, both byproducts of Insular innovations in book production during the eighth and ninth centuries; and as an outsider yet again in Carolingian scriptoria, where scribes preferred a modified version of <em>distinctiones</em> or a new system of liturgical punctuation called <em>positurae</em>. The history of the virgule reveals how the responsibilities once considered as acts of interpretation left to the reader became expectations for the appearance of texts placed upon the scribe.</p>

