<p>I present the history of the virgule or slash (/)as a measure of how students read and interpreted texts first by methods taught in grammar handbooks but then according to scribal-driven innovations to the page. The mark primarily operates in four ways: as an outsider to the dominant mode of punctuating from the first century CE to the ninth; as the term <em>virgula, </em>a “stem” or “twig” describing the shapes of accents or critical signs in grammatical treatises; as a run-over mark and byproduct of the Irish preference for content over calligraphy when copying manuscripts for export in the eighth and ninth centuries; and as a symbol perhaps descended from the acute accent and the neume <em>virga </em>in plainchant musical notation of the ninth to eleventh centuries. The history of the virgule reveals how the responsibilities once considered as acts of interpretation left to the reader became expectations for the appearance of texts placed upon the scribe.</p>

